export type EcCondition = (typeof EC_CONDITIONS_EN)[number];
export type EcSecondWord = (typeof EC_SECOND_WORD_EN)[number];

export function normalizeEasyChatWord(input: string): string {
  return input.trim().replace(/\s+/g, " ").toUpperCase();
}

export function isEcCondition(word: string): word is EcCondition {
  return (EC_CONDITIONS_EN as readonly string[]).includes(word);
}

export function isEcSecondWord(word: string): word is EcSecondWord {
  return (EC_SECOND_WORD_EN as readonly string[]).includes(word);
}
