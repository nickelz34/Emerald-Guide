/**
 * Extract Match Call rematch map locations from pokeemerald gRematchTable.
 * Writes src/data/matchCallSchedule.generated.ts
 *
 * Run: node scripts/extract-match-call-schedule.mjs
 */
import fs from "node:fs";
import path from "node:path";
import https from "node:https";

const ROOT = path.resolve(import.meta.dirname, "..");
const OUT = path.join(ROOT, "src/data/matchCallSchedule.generated.ts");
const RAW = "https://raw.githubusercontent.com/pret/pokeemerald/master/src/battle_setup.c";

function fetchText(url) {
  return new Promise((resolve, reject) => {
    https
      .get(url, (res) => {
        if (res.statusCode !== 200) {
          res.resume();
          reject(new Error(`HTTP ${res.statusCode}`));
          return;
        }
        const chunks = [];
        res.on("data", (c) => chunks.push(c));
        res.on("end", () => resolve(Buffer.concat(chunks).toString()));
      })
      .on("error", reject);
  });
}

function trainerIdToName(id) {
  return id
    .replace(/^TRAINER_/, "")
    .replace(/_\d+$/, "")
    .split("_")
    .map((w) => w.charAt(0) + w.slice(1).toLowerCase())
    .join(" ");
}

function mapIdToLabel(mapId) {
  const raw = mapId.replace(/^MAP_/, "");
  const route = raw.match(/^ROUTE(\d+)$/i);
  if (route) return `Route ${route[1]}`;
  const label = raw
    .split("_")
    .map((w) => w.charAt(0) + w.slice(1).toLowerCase())
    .join(" ");
  return label;
}

function parseRematchRows(text) {
  const tableMatch = text.match(
    /const struct RematchTrainer gRematchTable\[REMATCH_TABLE_ENTRIES\]\s*=\s*\{([\s\S]*?)\n\};/,
  );
  if (!tableMatch) throw new Error("gRematchTable not found");
  const rows = [];
  const re = /REMATCH\(([^)]+)\)/g;
  let m;
  while ((m = re.exec(tableMatch[1]))) {
    const parts = m[1].split(",").map((s) => s.trim());
    const mapId = parts[parts.length - 1];
    const trainerId = parts.find((p) => p.startsWith("TRAINER_"));
    if (!trainerId || !mapId.startsWith("MAP_")) continue;
    rows.push({
      trainerId: trainerId.replace(/_\d+$/, "_1").replace(/^(TRAINER_[A-Z_]+)_1$/, (_, base) => {
        // keep first rematch id form for linking
        return parts.find((p) => p.startsWith("TRAINER_")) ?? trainerId;
      }),
      trainerKey: trainerId.replace(/_\d+$/, ""),
      name: trainerIdToName(trainerId),
      mapId,
      location: mapIdToLabel(mapId),
      isGymLeader: /TRAINER_(ROXANNE|BRAWLY|WATTSON|FLANNERY|NORMAN|WINONA|TATE_AND_LIZA|JUAN|SIDNEY|PHOEBE|GLACIA|DRAKE|WALLACE)/.test(
        trainerId,
      ),
    });
  }
  // Dedupe by trainerKey (twins share one row)
  const seen = new Map();
  for (const row of rows) {
    if (!seen.has(row.trainerKey)) seen.set(row.trainerKey, row);
  }
  return [...seen.values()].sort((a, b) => a.name.localeCompare(b.name));
}

async function main() {
  const text = await fetchText(RAW);
  const rows = parseRematchRows(text);
  const body = `/** Auto-generated by scripts/extract-match-call-schedule.mjs — do not edit. */
export interface MatchCallScheduleRow {
  trainerId: string;
  trainerKey: string;
  name: string;
  mapId: string;
  location: string;
  isGymLeader: boolean;
}

export const MATCH_CALL_SCHEDULE_ROWS: MatchCallScheduleRow[] = ${JSON.stringify(rows, null, 2)};
`;
  fs.writeFileSync(OUT, body);
  console.log(`Wrote ${rows.length} rematch locations to ${path.relative(ROOT, OUT)}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
