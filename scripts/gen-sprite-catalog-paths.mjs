#!/usr/bin/env node
/** Scan public/sprites and write src/admin/spriteCatalogPaths.ts */
import fs from "fs";
import path from "path";

function walk(dir, base = dir) {
  const out = [];
  for (const name of fs.readdirSync(dir)) {
    const full = path.join(dir, name);
    const rel = path.relative(base, full).replaceAll("\\", "/");
    if (fs.statSync(full).isDirectory()) out.push(...walk(full, base));
    else if (/\.(png|gif|webp|jpe?g)$/i.test(name)) out.push(rel);
  }
  return out;
}

const files = walk("public/sprites", "public").sort();
const trainers = files.filter((f) => f.startsWith("sprites/trainers/"));
const items = files.filter((f) => f.startsWith("sprites/items/"));
const other = files.filter(
  (f) =>
    !f.startsWith("sprites/pokemon/emerald/") &&
    !f.startsWith("sprites/trainers/") &&
    !f.startsWith("sprites/items/icons/"),
);

const lines = [];
lines.push("// AUTO-GENERATED by scripts/gen-sprite-catalog-paths.mjs — do not edit by hand.");
lines.push("/** Relative paths under public/ for non-Pokémon project sprites. */");
lines.push("export const TRAINER_SPRITE_PATHS: string[] = [");
for (const f of trainers) lines.push(`  ${JSON.stringify(f)},`);
lines.push("];");
lines.push("");
lines.push("export const ITEM_SPRITE_PATHS: string[] = [");
for (const f of items) lines.push(`  ${JSON.stringify(f)},`);
lines.push("];");
lines.push("");
lines.push("export const OTHER_SPRITE_PATHS: string[] = [");
for (const f of other) lines.push(`  ${JSON.stringify(f)},`);
lines.push("];");
lines.push("");

fs.writeFileSync("src/admin/spriteCatalogPaths.ts", lines.join("\n"));
console.log(
  `Wrote spriteCatalogPaths.ts (${trainers.length} trainers, ${items.length} items, ${other.length} other)`,
);
