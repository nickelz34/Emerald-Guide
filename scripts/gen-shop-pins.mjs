/**
 * Generate outdoor shop landmark pins from pokeemerald + the Hoenn composite
 * manifest — same tile→% formula as mapPointsGenerated / gen-map-crops.
 *
 * Entrances that are already shops (Mart, Herb Shop, Flower Shop, etc.) stay in
 * mapPointsGenerated. This file only covers outdoor storefronts that are NOT
 * warp entrances (e.g. Slateport Market stalls).
 *
 * Usage: node scripts/gen-shop-pins.mjs
 */
import fs from "node:fs";
import path from "node:path";

const ROOT = path.resolve(import.meta.dirname, "..");
const REPO = path.join(ROOT, ".calib/pokeemerald");
const MANIFEST = path.join(ROOT, ".calib/manifest.json");
const OUT = path.join(ROOT, "src/data/shopPinsGenerated.ts");

/** Outdoor shops that need a dedicated pin (not a building entrance). */
const SHOP_PIN_DEFS = [
  {
    id: "shop-slateport-market",
    name: "Market",
    note: "Slateport City",
    mapName: "SlateportCity",
    // Prefer the Market sign; fall back to Energy Guru stall.
    find: (map) => {
      const sign = (map.bg_events || []).find((b) => /MarketSign/i.test(b.script || ""));
      if (sign) return { x: sign.x, y: sign.y, source: "bg:MarketSign" };
      const guru = (map.object_events || []).find((o) => /EnergyGuru/i.test(o.script || ""));
      if (guru) return { x: guru.x, y: guru.y, source: "obj:EnergyGuru" };
      return null;
    },
  },
];

const round2 = (n) => Math.round(n * 100) / 100;

function tileToMapPct(gx, gy, tx, ty, wTiles, hTiles) {
  return {
    x: round2(((gx + tx + 0.5) / wTiles) * 100),
    y: round2(((gy + ty + 0.5) / hTiles) * 100),
  };
}

function main() {
  if (!fs.existsSync(MANIFEST)) {
    console.error("Missing .calib/manifest.json");
    process.exit(1);
  }
  if (!fs.existsSync(REPO)) {
    console.error("Missing .calib/pokeemerald");
    process.exit(1);
  }

  const manifest = JSON.parse(fs.readFileSync(MANIFEST, "utf8"));
  const { wTiles, hTiles } = manifest;
  const byName = new Map(manifest.maps.map((m) => [m.name, m]));

  const points = [];
  for (const def of SHOP_PIN_DEFS) {
    const meta = byName.get(def.mapName);
    if (!meta) {
      console.error(`Map ${def.mapName} not on outdoor composite`);
      process.exit(1);
    }
    const mapPath = path.join(REPO, "data/maps", def.mapName, "map.json");
    const map = JSON.parse(fs.readFileSync(mapPath, "utf8"));
    const hit = def.find(map);
    if (!hit) {
      console.error(`Could not locate anchor for ${def.id} on ${def.mapName}`);
      process.exit(1);
    }
    const pct = tileToMapPct(meta.gx, meta.gy, hit.x, hit.y, wTiles, hTiles);
    points.push({
      id: def.id,
      name: def.name,
      category: "shop",
      x: pct.x,
      y: pct.y,
      note: def.note,
      _source: hit.source,
      _tile: `${hit.x},${hit.y}`,
    });
    console.log(`${def.id}: ${pct.x}, ${pct.y} (${hit.source} @ ${hit.x},${hit.y})`);
  }

  const lines = [];
  lines.push("// AUTO-GENERATED by scripts/gen-shop-pins.mjs — do not edit by hand.");
  lines.push("// Outdoor shop landmarks from pokeemerald map.json + .calib/manifest.json.");
  lines.push('import type { MapPoint } from "./mapPoints";');
  lines.push("");
  lines.push("export const SHOP_PINS_GENERATED: MapPoint[] = [");
  for (const p of points) {
    lines.push(
      `  { id: ${JSON.stringify(p.id)}, name: ${JSON.stringify(p.name)}, category: "shop", x: ${p.x}, y: ${p.y}, note: ${JSON.stringify(p.note)} }, // ${p._source} tile ${p._tile}`,
    );
  }
  lines.push("];");
  lines.push("");

  fs.writeFileSync(OUT, lines.join("\n"), "utf8");
  console.log(`Wrote ${path.relative(ROOT, OUT)} (${points.length} pins)`);
}

main();
