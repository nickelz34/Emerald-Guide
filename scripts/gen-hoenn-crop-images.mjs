/**
 * Extract walkthrough-sized windows from public/maps/hoenn-map.png so HoennCrop
 * can load a few-hundred-KB crop instead of the full ~28MB composite on mobile.
 *
 * Run: node scripts/gen-hoenn-crop-images.mjs
 */
import fs from "node:fs";
import path from "node:path";
import sharp from "sharp";

const ROOT = path.resolve(import.meta.dirname, "..");
const SRC = path.join(ROOT, "public/maps/hoenn-map.png");
const OUT_DIR = path.join(ROOT, "public/maps/crops");
const MAP_CROPS_TS = path.join(ROOT, "src/data/mapCrops.ts");
const OUT_TS = path.join(ROOT, "src/data/hoennCropImages.ts");

const HOENN_MAP_W = 12800;
const HOENN_MAP_H = 6128;

function parseAreaMapCrop() {
  const text = fs.readFileSync(MAP_CROPS_TS, "utf8");
  const block = text.match(/export const AREA_MAP_CROP[^=]*=\s*\{([\s\S]*?)\n\};/);
  if (!block) throw new Error("Could not parse AREA_MAP_CROP from mapCrops.ts");

  const areas = {};
  const re =
    /"([^"]+)":\s*\{\s*crop:\s*\{\s*x:\s*([\d.]+),\s*y:\s*([\d.]+),\s*w:\s*([\d.]+),\s*h:\s*([\d.]+)\s*\}/g;
  for (const m of block[1].matchAll(re)) {
    areas[m[1]] = {
      x: Number(m[2]),
      y: Number(m[3]),
      w: Number(m[4]),
      h: Number(m[5]),
    };
  }
  return areas;
}

function cropKey(crop) {
  const r = (n) => Math.round(n * 10000) / 10000;
  return `${r(crop.x)}_${r(crop.y)}_${r(crop.w)}_${r(crop.h)}`;
}

function cropPixels(crop) {
  const left = Math.max(0, Math.round((crop.x / 100) * HOENN_MAP_W));
  const top = Math.max(0, Math.round((crop.y / 100) * HOENN_MAP_H));
  const width = Math.min(HOENN_MAP_W - left, Math.round((crop.w / 100) * HOENN_MAP_W));
  const height = Math.min(HOENN_MAP_H - top, Math.round((crop.h / 100) * HOENN_MAP_H));
  return { left, top, width, height };
}

async function main() {
  if (!fs.existsSync(SRC)) {
    console.error(`Missing ${SRC} — run render:hoenn first.`);
    process.exit(1);
  }

  const areas = parseAreaMapCrop();
  fs.mkdirSync(OUT_DIR, { recursive: true });

  const byKey = new Map();
  for (const [areaId, crop] of Object.entries(areas)) {
    const key = cropKey(crop);
    if (!byKey.has(key)) byKey.set(key, { crop, areaIds: [] });
    byKey.get(key).areaIds.push(areaId);
  }

  const areaToImage = {};
  const keyToFile = {};

  for (const [key, { crop, areaIds }] of byKey) {
    const fileName = `${areaIds[0]}.png`;
    const outPath = path.join(OUT_DIR, fileName);
    const { left, top, width, height } = cropPixels(crop);

    await sharp(SRC)
      .extract({ left, top, width, height })
      .png({ compressionLevel: 9, effort: 10 })
      .toFile(outPath);

    keyToFile[key] = `maps/crops/${fileName}`;
    for (const areaId of areaIds) {
      areaToImage[areaId] = keyToFile[key];
    }

    const kb = (fs.statSync(outPath).size / 1024).toFixed(1);
    console.log(`${fileName}  ${width}×${height}px  ${kb} KB  (${areaIds.length} areas)`);
  }

  const lines = [
    "// AUTO-GENERATED by scripts/gen-hoenn-crop-images.mjs — do not edit by hand.",
    "// Pre-cut walkthrough windows from public/maps/hoenn-map.png (pixel-identical to CSS crops).",
    "",
    "/** Outdoor walkthrough area id → pre-rendered crop image under public/. */",
    "export const HOENN_CROP_IMAGE_BY_AREA: Record<string, string> = {",
  ];

  for (const [areaId, image] of Object.entries(areaToImage).sort(([a], [b]) => a.localeCompare(b))) {
    lines.push(`  ${JSON.stringify(areaId)}: ${JSON.stringify(image)},`);
  }

  lines.push("};", "");
  lines.push("/** Resolve a pre-cut crop image for walkthrough HoennCrop, if available. */");
  lines.push("export function hoennCropImagePath(areaId?: string): string | null {");
  lines.push("  if (!areaId) return null;");
  lines.push("  return HOENN_CROP_IMAGE_BY_AREA[areaId] ?? null;");
  lines.push("}", "");

  fs.writeFileSync(OUT_TS, lines.join("\n"));
  console.log(`\nWrote ${Object.keys(areaToImage).length} area mappings → ${OUT_TS}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
